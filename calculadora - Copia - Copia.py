valor1 = float(input('Digite o primeiro valor:'))
valor2 = float(input('Digite o segundo valor:'))
operacao = input('Digite a operação aritmética desejada: (+ , - , * , / ) ')
if operacao  =='+':
    print(valor1+ valor2)
elif operacao =='-':
    print(valor1 - valor2)
elif operacao =='*':
    print(valor1 * valor2)
elif operacao =='/':
    print(valor1 / valor2)
